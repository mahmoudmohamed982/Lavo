const setLanguage = (language) => {
    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach((element) => {
        const translationKey = element.getAttribute("data-i18n");
        element.innerHTML = translations[language][translationKey];
    });

    // lang realted styles
    if (language === "ar") {
        document.dir = "rtl";
        document.body.style.textAlign = 'right';
        document.body.style.fontFamily = 'Tajawal';
        document.getElementById("fullName").placeholder = "اسم الكريم";
        document.getElementById("phoneNumber").placeholder = "رقم الجوال";



    } else {
        document.dir = "ltr"
        document.body.style.textAlign = 'left';
        document.body.style.fontFamily = 'Poppins';

    };

};

